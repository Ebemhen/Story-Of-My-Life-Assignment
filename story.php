      <h1>My University Experience</h1>
      <p>
        It is a common notion, and indeed a fact that university life is a
        stressful one and also filled with lots of adventures. So far, my
        experience has been a beautiful one altogether. My academic experience
        in UI began with taking virtual classes due to pandemic that occurred
        the previous year. It was something I was experiencing for the first
        time, and definitely not what I expected as my first experience in UI. A
        large part of my first semester was online, including my matriculation.
        I got to build a relationship with a number of course mates and
        lecturers even before meeting physically them. Being in UI has opened me
        up to a lot of opportunities in my field of study, career path, and in
        other areas. I have met really wonderful personalities as friends,
        leaders, and lecturers. I have few years left to spend in UI, and I'm
        looking forward to making the most out of the time left.
