      <h1>Story Of My Life</h1>
      <p>
        I am Nwabuife Divine Oluchukwu, a young nigerian lady currently taking
        an undergraduate course in Computer Science in the University of Ibadan,
        Nigeria. I was born in Edo State, and lived in Benin up until adult age.
        I had barely travelled out of Benin throughout my childhood and teenage
        days, and so I was very intentional about choosing a University outside
        Edo State, and also one that would offer quality education alongside
        good opportunities and a wonderful experience. Hence, I chose University
        of Ibadan. I was born into a family of 6, with caring, God-fearing and
        responsible parents, and three lovely elder siblings. I grew up knowing
        God and learning to live for Him, and He has been the essence of my
        living and the one who has led me through up till now. So far, I have
        had several highs and lows in life that have taught me lessons and
        shaped me into who I am today. Also, I have been blessed with beautiful
        relationships, ranging from family, to friends, leaders, teachers, etc.
        Indeed, the present version of me is a result of God's love, a lovely
        family, priceless relationships, and the lessons I have learnt from
        every challenge I have faced. I am grateful for the before and the
        present, and I look enthusiastic about what's ahead.
      </p>
