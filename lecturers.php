      <h1>Dr. Aderonke Busay Sakpere</h1>
      <p>
        Dr. Aderonke Sapkere received both BSc and Msc degrees in Computer
        Science from University of Ilorin, Nigeria. She holds a PhD in Computer
        Science from the university of Cape Town, South Africa. She is an Oracle
        PL/SQL Developer Certified Associate. She is a faculty member of the
        University of Ibadan with over 10 years of experience in lecturing,
        research, and mentoring. As a lecturer, she teaches both undergraduate
        and postgraduate studies. She has won various award over the years. They
        include MIT Empowering The Teachers Fellowship, Selected Young Scientist
        by Lindau Nobel Laureate Meetings, among others. She is the founder of
        Tech Girls Club which was setup to foster networking and interaction
        among females in technology and also the co-founder of Lindau Nobel
        Laureate Meetings in Germany. Outside of academics and tech life, she
        loves to try new recipe. She has a book titled "Bringing the Restaurant
        to your Home", and it is published on Amazon.
