      <p>
        Dr. Babatunde Ayinla is a lecturer in the Computer Science department of
        the University of Ibadan. Dr Ayinla teaches both undergraduate and
        postgraduate students. He is well versed in the knowledge of software
        engineering, and this has continuously been shown during his lectures.
        Dr Ayinla is a strong poineer of his students having basic knowledge
        about programming, irrespective of their field of study/specialization.
        He puts measures in place to ensure that his students do not shy away
        from problem solving but rather take delight in it. Dr Ayinla is very
        passionate about his students being excellent in what they do. One of
        his beliefs is that, a Computer Science student should be easily
        differentiated from a roadside programmer by the professionalism and
        excellency their work.
